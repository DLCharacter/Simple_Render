//Vertex class
package Util;
public class Vertex_3D
{
    private int x;
    private int y;
    private int z;
    public Vertex_3D(int x, int y, int z)
    {
        this.x=x;
        this.y=y;
        this.z=z;
    }
    public int getX()
    {
        return x;
    }
    public int getY()
    {
        return y;
    }
    public int getZ()
    {
        return z;
    }
}
